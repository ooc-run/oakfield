/**
 * @file digamma.c
 * @brief Real and complex digamma implementations with fixed, adaptive, and Mortici tails.
 *
 * The implementations use recurrence to shift arguments into an asymptotic
 * region, reflection identities for negative real/complex inputs when enabled,
 * and Bernoulli/Stirling tails for the final approximation. Public variants
 * differ mainly in the number of tail coefficients or the speed/accuracy trade.
 */

#include "internal/polygamma_internal.h"

static inline double digamma_core_f64(double x, int tail_terms);
static inline float  digamma_core_f32(float x, int tail_terms);
static SimComplexDouble digamma_core_c64(SimComplexDouble z, int tail_terms);

static inline double digamma_mid_1_2_f64(double x) {
    /* Generated by tools/approx/oakfield_minimax for digamma on [1, 2].
     * Original coefficients are Chebyshev-basis rational (6, 5); these are
     * transformed to powers of y = x - 1 for Horner evaluation.
     */
    const double y = x - 1.0;

    const double p = FMA(y,
                         FMA(y,
                             FMA(y,
                                 FMA(y,
                                     FMA(y,
                                         FMA(y, 6.5127627380449206e-06,
                                             0.0020639282645390053),
                                         0.042298695568535903),
                                     0.25140297896749503),
                                 0.50637604662505098),
                             0.18120872899074339),
                         -0.21825887618751916);
    const double q = FMA(y,
                         FMA(y,
                             FMA(y,
                                 FMA(y,
                                     FMA(y, 0.00047048130918966998,
                                         0.015253188176546778),
                                     0.14073112542092631),
                                 0.51145573085839002),
                             0.76363085805570574),
                         0.37812361905450348);
    return p / q;
}

static inline double digamma_stirling_f64(double x, int tail_terms) {
    const double inv  = 1.0 / x;
    const double inv2 = inv * inv;
    double       series = 0.0;
    double       inv2n  = inv2;
    int          N      = tail_terms;
    int          maxN   = (int) (sizeof(C2) / sizeof(C2[0]));
    if (N > maxN)
        N = maxN;
    for (int n = 1; n <= N; ++n) {
        series = FMA(C2[n - 1], inv2n, series);
        inv2n *= inv2;
    }
    return log(x) - 0.5 * inv + series;
}

static inline double digamma_large_f64(double x, int tail_terms) {
    if (tail_terms < 7)
        return digamma_stirling_f64(x, tail_terms);

    x -= 1.0;
    const double inv = 1.0 / x;
    const double z   = inv * inv;
    const double p   = FMA(z,
                         FMA(z,
                             FMA(z,
                                 FMA(z,
                                     FMA(z,
                                         FMA(z,
                                             FMA(z, -0.44325980392156863,
                                                 0.083333333333333333),
                                             -0.021092796092796094),
                                         0.0075757575757575758),
                                     -0.0041666666666666666),
                                 0.0039682539682539683),
                             -0.0083333333333333332),
                         0.083333333333333333);
    return log(x) + 0.5 * inv - z * p;
}

