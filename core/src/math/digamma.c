/**
 * @brief Evaluate complex double digamma by increasing tail length until convergence.
 *
 * The routine compares successive fixed-tail evaluations and stops when the
 * component-wise change is below `tol`, or after the configured maximum tail
 * sweep.
 *
 * @param z Complex argument.
 * @param tol Absolute convergence threshold on successive approximations.
 * @return Best complex approximation found by the tail sweep.
 */
SimComplexDouble sim_digamma_c64_tail(SimComplexDouble z, double tol) {
    int              N    = 3;
    SimComplexDouble last = digamma_core_c64(z, N);
    for (; N < 60; ++N) {
        SimComplexDouble cur = digamma_core_c64(z, N);
        if (fabs(cur.re - last.re) + fabs(cur.im - last.im) < tol)
            return cur;
        last = cur;
    }
    return last;
}
